export class Pessoa {
  static idadePadrao = 0;
  static cpfPadrao = '000.000.000-00';

  constructor(
    public nome: string,
    public sobrenome: string,
    public idade: number,
    public cpf: string,
  ) {}

  static criaPessoa(nome: string, sobrenome: string): Pessoa {
    return new Pessoa(nome, sobrenome, Pessoa.idadePadrao, Pessoa.cpfPadrao);
  }
}

const pessoa1 = new Pessoa('Gabriel', 'Lucas', 23, '111.111.111-11');
const pessoa2 = Pessoa.criaPessoa('Gabriel', 'Lucas');
console.log(pessoa1);
console.log(pessoa2);
